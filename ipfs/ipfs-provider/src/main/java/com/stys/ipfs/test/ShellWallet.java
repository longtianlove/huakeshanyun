package com.stys.ipfs.test;

public class ShellWallet {
	private int index;
	private int coinType;
	private String privateKey;
	private String publickey;
	private String address;
	private String currency;

	public ShellWallet(int index, int coinType, String privateKey, String publickey, String address, String currency) {
		this.index = index;
		this.coinType = coinType;
		this.privateKey = privateKey;
		this.publickey = publickey;
		this.address = address;
		this.currency = currency;
	}

	public ShellWallet() {
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public int getCoinType() {
		return coinType;
	}

	public void setCoinType(int coinType) {
		this.coinType = coinType;
	}

	public String getPrivateKey() {
		return privateKey;
	}

	public void setPrivateKey(String privateKey) {
		this.privateKey = privateKey;
	}

	public String getPublickey() {
		return publickey;
	}

	public void setPublickey(String publickey) {
		this.publickey = publickey;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getCurrency() {
		return currency;
	}

	public void setCurrency(String currency) {
		this.currency = currency;
	}

	@Override
	public String toString() {
		return "ShellWallet{" + "index=" + index + ", coinType=" + coinType + ", privateKey='" + privateKey + '\''
				+ ", publickey='" + publickey + '\'' + ", address='" + address + '\'' + ", name='" + currency + '\''
				+ '}';
	}
}
